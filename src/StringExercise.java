public class StringExercise {
    public static void main(String[] args) {
        String first = "We don't need no education\nWe don't need no thought control";
        System.out.println(first);

        String second = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(second);

        String third = "In windows, the main drive is usually C:\\";
        System.out.println(third);

        String fourth = "I can do backslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\!";
        System.out.println(fourth);

    }
}
